public class escape_sequence {
    public static void main(String[] args) {
//        Escape Sequence	     Description
//            \t	             Insert a tab in the text at this point.
//            \b	             Insert a backspace in the text at this point.
//            \n	             Insert a newline in the text at this point.
//            \r	             Insert a carriage return in the text at this point.
//            \f	             Insert a form feed in the text at this point.
//            \'	             Insert a single quote character in the text at this point.
//            \"	             Insert a double quote character in the text at this point.
//            \\	             Insert a backslash character in the text at this point.
        System.out.println("India is my\tcountry");
        System.out.println("India is my \bcountry");
        System.out.println("India is my \ncountry");
        System.out.println("India is \rmy country");
        System.out.println("India is \fmy country");
        System.out.println("India is \'my country");
        System.out.println("India is \"my country");
        System.out.println("India is \\my country");
    }
}

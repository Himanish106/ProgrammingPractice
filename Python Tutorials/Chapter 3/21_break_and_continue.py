# Break and continue statement logic similar as other programming languages

# 1) break statement

for i in range(1, 11):
    if i == 5:
        break
    print(i)
print("..........................\n")
# 2) continue statement
# print 1 to 10, but not 5

for j in range(1, 11):
    if j == 5:
        continue
    print(j)

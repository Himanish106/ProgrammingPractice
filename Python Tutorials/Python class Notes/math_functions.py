import math
# Algebraic functions
print("exp(1.0) = ", math.exp(1))  # --> exp(1.0) =  2.718281828459045
print("log(2.78) = ", math.log(math.e))
print("log(10,10) = ", math.log(10, 10))
print("sqrt(4.0) = ", math.sqrt(4.0))
print("fabs(-2) = ", math.fabs(-2))  # ---> fabs(-2) =  2.0
print("abs(-2) = ", abs(-2))
# Both abs and fabs are used to get the absolute value. The difference between this is fabs is used on mathematical functions while abs is an built in function and also fabs returns the value in float while abs return in integer
print("ceil(2.1) = ", math.ceil(2.1))  # ---> ceil(2.1) =  3
print("ceil(-2.1) = ", math.ceil(-2.1))  # ---> ceil(-2.1) =  -2
print("floor(2.1) = ", math.floor(2.1))  # --> floor(2.1) =  2
print("floor(-2.1) = ", math.floor(-2.1))  # --> floor(-2.1) =  -3

# Logarithimic functions
print("sin(PI/2) = ", math.sin(math.pi/2))
print("cos(PI/2) = ", math.cos(math.pi/2))
print("tan(PI/2) = ", math.tan(math.pi/2))
print("degrees(1.57) = ", math.degrees(1.57))
print("radians(90) = ", math.radians(90))

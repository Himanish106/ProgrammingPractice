# Binary Conversions

# 1) Decimal to Binary
x = 15
print(bin(x))  # ----> 0b1111

# 2) Octal to Binary
a = 0o123
print(bin(a))  # ----> 0b1010011

# 3) Hexadecimal to Binary
b = 0xface
print(bin(b))

# Octal Conversions

# 1) Decimal to Octal
c = 100
print(oct(c))

# 2) Binary to Octal
d = 0b111101
print(oct(d))

# 3) Hexadecimal to Octal
e = 0xface
print(oct(e))

# Hexadecimal Conversions
# 1) Decimal to Hexadecimal
f = 1000
print(hex(f))

# 2) Binary to Hexadecimal
g = 0b111101
print(hex(g))

# 3) Octal to Hexadecimal
h = 0o123456
print(hex(h))

# Adding items to list
fruits = ['grapes', 'pineapple']
fruits.append('mango') # ---> In append method added element is added at the end of the list
print(fruits)

# In real life we use append method like this:
vegetable = []  # --> An empty list is given as initially we don't know how many elements we need to add
vegetable.append("Onion")
vegetable.append("Cabbage")
vegetable.append("Cucumber")
vegetable.append("Carrot")
print(vegetable)
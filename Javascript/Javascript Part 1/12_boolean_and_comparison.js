// booleans & comparison operator

// booleans
// true, false

let num1 = 7;
let num2 = "9";

console.log(num1 < num2); // true ---> This is possible only in javascript. Does not matter if it is a string. Only value is the concern.

// == vs ===
num3 = 15;
num4 = "15";
console.log(num3 == num4); // true as data type is not a concern
console.log(num3 === num4); // false as data type is a concern

// != vs !==

console.log(num3 != num4) // false as data type is not a concern

console.log(num3 !== num4) // true as data type is a concern
